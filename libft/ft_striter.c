void	ft_striter(char *s, void (*f)(char *))
{
	int		i;

	if (!(s) || !(f))
		return ;
	if (s && f)
	{
		i = 0;
		while (s[i] != 0)
		{
			f(&s[i]);
			i++;
		}
	}
}
